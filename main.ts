const countRedundancies = (strs: string[]) => {
  const counts = new Map<string, number>();

  for (const s of strs) {
    counts.set(s, (counts.get(s) || 0) + 1);
  }

  counts.forEach((v, k) => {
    if (v > 1) console.log(k);
  });
};

